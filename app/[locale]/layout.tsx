import { Geist, Geist_Mono } from 'next/font/google'
import '../globals.css'
import ClientProviders from '@/components/shared/client-providers'
import { getDirection } from '@/i18n-config'
import { NextIntlClientProvider } from 'next-intl'
import { getMessages } from 'next-intl/server'
import { routing } from '@/i18n/routing'
import { notFound } from 'next/navigation'
import { cookies } from 'next/headers'
import { getSetting } from '@/lib/actions/setting.actions'

const geistSans = Geist({
  variable: '--font-geist-sans',
  subsets: ['latin'],
})

const geistMono = Geist_Mono({
  variable: '--font-geist-mono',
  subsets: ['latin'],
})

/**
 * Generates metadata for the root layout.
 *
 * @returns {Promise<Metadata>} The metadata to be used for the root layout.
 *
 * The metadata includes the title, description, and metadata base URL.
 * The title is a template that includes the name of the site and its slogan.
 * The description is the description of the site.
 * The metadata base URL is the URL of the site.
 */
export async function generateMetadata() {
  const {
    site: { slogan, name, description, url },
  } = await getSetting()
  return {
    title: {
      template: `%s | ${name}`,
      default: `${name}. ${slogan}`,
    },
    description: description,
    metadataBase: new URL(url),
  }
}

/**
 * AppLayout is the root layout component for the application.
 * It sets up the HTML document structure, including language
 * and text direction based on locale. It provides internationalization
 * and client-specific settings to the application.
 *
 * @param {Object} params - The parameters object.
 * @param {string} params.locale - The locale string to determine language and direction.
 * @param {React.ReactNode} children - The children components to render inside the layout.
 * @returns {JSX.Element} The layout component wrapped with necessary providers.
 */
export default async function AppLayout({
  params,
  children,
}: {
  params: { locale: string }
  children: React.ReactNode
}) {
  const setting = await getSetting()
  const currencyCookie = (await cookies()).get('currency')
  const currency = currencyCookie ? currencyCookie.value : 'USD'

  const { locale } = await params
  // Ensure that the incoming `locale` is valid
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  if (!routing.locales.includes(locale as any)) {
    notFound()
  }
  const messages = await getMessages()

  return (
    <html
      lang={locale}
      dir={getDirection(locale) === 'rtl' ? 'rtl' : 'ltr'}
      suppressHydrationWarning
    >
      <body
        className={`min-h-screen ${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        <NextIntlClientProvider locale={locale} messages={messages}>
          <ClientProviders setting={{ ...setting, currency }}>
            {children}
          </ClientProviders>
        </NextIntlClientProvider>
      </body>
    </html>
  )
}
